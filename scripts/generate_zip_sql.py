DELTA = 0.05

def row(zip_int, parent_id, lat, lng):
    name = "ZIP {}".format(zip_int)
    min_lat = round(lat - DELTA, 2)
    max_lat = round(lat + DELTA, 2)
    min_lng = round(lng - DELTA, 2)
    max_lng = round(lng + DELTA, 2)
    return "  (2, {}, {}, '{}', {}, {}, {}, {}, {}, {})".format(
        zip_int, parent_id, name, min_lat, max_lat, min_lng, max_lng, round(lat, 2), round(lng, 2)
    )

rows = []
for parent_id, ranges in RANGES:
    lat, lng = CENTERS[parent_id]
    for lo, hi in ranges:
        for z in range(lo, hi + 1):
            rows.append(row(z, parent_id, lat, lng))

print("-- Local (ZIP): code_int = 5-digit zip. parent_id = state row id (2=AL, 3=AK, ..., 52=WY).")
print("INSERT OR IGNORE INTO location_areas (location_type, code_int, parent_id, name, min_lat, max_lat, min_lng, max_lng, center_lat, center_lng) VALUES")
print(",\n".join(rows) + ";")
